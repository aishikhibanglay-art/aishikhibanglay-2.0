import { useState, useEffect } from "react";
import { Link } from "wouter";
import { supabase } from "@/lib/supabase";
import PublicLayout from "@/layouts/PublicLayout";
import { SEO } from "@/components/SEO";
import { FileText, ChevronRight } from "lucide-react";

interface PolicyPageProps {
  slug: "privacy-policy" | "terms" | "refund-policy" | "cookie-policy";
  title: string;
  seoDesc: string;
  richContent: React.ReactNode;
}

const PROSE =
  "prose prose-invert prose-base max-w-none " +
  "prose-headings:text-white prose-headings:font-bold prose-headings:mt-8 prose-headings:mb-3 " +
  "prose-h2:text-xl prose-h3:text-lg prose-h4:text-base " +
  "prose-p:text-gray-300 prose-p:leading-relaxed prose-p:mb-4 " +
  "prose-a:text-violet-400 prose-a:no-underline hover:prose-a:underline " +
  "prose-strong:text-white prose-strong:font-semibold " +
  "prose-ul:text-gray-300 prose-ol:text-gray-300 prose-li:marker:text-violet-400 " +
  "prose-hr:border-gray-800";

function PolicyPage({ slug, title, seoDesc, richContent }: PolicyPageProps) {
  const [dbContent, setDbContent] = useState<string | null>(null);
  const [updatedAt, setUpdatedAt] = useState<string | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    supabase
      .from("custom_pages")
      .select("content,updated_at")
      .eq("slug", slug)
      .eq("is_published", true)
      .single()
      .then(({ data }) => {
        // Only use DB content if admin has written substantial content (>500 chars)
        if (data?.content && data.content.length > 500) {
          setDbContent(data.content);
          setUpdatedAt(data.updated_at);
        }
        setLoading(false);
      });
  }, [slug]);

  return (
    <PublicLayout>
      <SEO title={title} description={seoDesc} url={`/${slug}`} />

      {/* Hero */}
      <section className="bg-gradient-to-b from-gray-900 to-gray-950 py-16 border-b border-gray-800/60">
        <div className="max-w-3xl mx-auto px-4 sm:px-6 lg:px-8 text-center">
          <div className="inline-flex items-center justify-center w-14 h-14 rounded-2xl bg-violet-500/15 mb-5">
            <FileText className="w-7 h-7 text-violet-400" />
          </div>
          <h1 className="text-3xl sm:text-4xl font-extrabold text-white mb-3">{title}</h1>
          <p className="text-sm text-gray-500">
            সর্বশেষ আপডেট:{" "}
            {updatedAt
              ? new Date(updatedAt).toLocaleDateString("bn-BD", { year: "numeric", month: "long", day: "numeric" })
              : "৭ এপ্রিল, ২০২৬"}
          </p>
        </div>
      </section>

      {/* Breadcrumb */}
      <div className="max-w-3xl mx-auto px-4 sm:px-6 lg:px-8 pt-6">
        <nav className="flex items-center gap-2 text-sm text-gray-500">
          <Link href="/" className="hover:text-violet-400 transition-colors">হোম</Link>
          <ChevronRight className="w-4 h-4" />
          <span className="text-violet-400">{title}</span>
        </nav>
      </div>

      <div className="max-w-3xl mx-auto px-4 sm:px-6 lg:px-8 py-10 pb-16">
        {loading ? (
          <div className="space-y-4 animate-pulse">
            {[...Array(10)].map((_, i) => (
              <div key={i} className={`h-4 bg-gray-800 rounded ${i % 5 === 0 ? "h-6 w-2/3 mt-8" : i % 3 === 0 ? "w-3/4" : "w-full"}`} />
            ))}
          </div>
        ) : dbContent ? (
          // Admin has written full content — show it
          <article className={PROSE} dangerouslySetInnerHTML={{ __html: dbContent }} />
        ) : (
          // Use rich built-in content
          <div className={PROSE}>{richContent}</div>
        )}
      </div>
    </PublicLayout>
  );
}

